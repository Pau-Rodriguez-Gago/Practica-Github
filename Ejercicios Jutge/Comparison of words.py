palabras=input("").split()
palabra1=palabras[0]
palabra2=palabras[1]
if palabra1<palabra2:
    print(palabra1,"<",palabra2)
elif palabra1>palabra2:
    print(palabra1,">",palabra2)
else:
    print(palabra1,"=",palabra2)
